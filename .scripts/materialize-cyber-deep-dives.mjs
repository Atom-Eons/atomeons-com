/**
 * Materialize the cyber-deep-dives-50 workflow output into TSX route files.
 *
 * Workflow wf_dd652968-46e produces a JSON with `shippable` array of
 * ContentPage objects with slug + title + subtitle + intro + sections +
 * citations [+ crosslinks + accent].
 *
 * Each one materializes to:
 *   app/learn/cyber/{slug}/page.tsx
 *
 * Using the existing GenericPage renderer pattern. ContentPage shape
 * matches what GenericPage already accepts.
 *
 * Usage:
 *   node .scripts/materialize-cyber-deep-dives.mjs <workflow-output-json-path>
 *
 * Or auto-discovers from C:\Users\a\AppData\Local\Temp\claude\...\tasks\wjui3e203.output
 *
 * Idempotent. Skips slugs that already have a page.tsx file.
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname, resolve } from "node:path";

const ROOT = resolve("app/learn/cyber");

const argInput = process.argv[2];
const DEFAULT_OUTPUT = "C:/Users/a/AppData/Local/Temp/claude/C--AtomEons--claude-worktrees-bold-leakey-4470e8/36c5895e-6dd9-41db-9f77-29d3975f016f/tasks/wjui3e203.output";

const inputPath = argInput || DEFAULT_OUTPUT;

if (!existsSync(inputPath)) {
  console.error(`ERROR: input not found at ${inputPath}`);
  console.error(`Pass the workflow output JSON path as the first arg.`);
  process.exit(2);
}

const raw = readFileSync(inputPath, "utf8");
const outer = JSON.parse(raw);
const result = outer.result || outer;
const pages = result.shippable || result.pages || [];

if (!Array.isArray(pages) || pages.length === 0) {
  console.error(`ERROR: no shippable pages found in workflow output.`);
  console.error(`Top-level keys: ${Object.keys(outer).join(", ")}`);
  if (outer.result) console.error(`result keys: ${Object.keys(outer.result).join(", ")}`);
  process.exit(2);
}

console.log(`Materializing ${pages.length} cyber deep-dive pages`);

// EXISTING pages we must NEVER overwrite. The cyber-deep-dives-50 workflow
// is instructed not to duplicate these but we double-check.
const EXISTING_PAGES = new Set([
  "path", "labs", "hackerone", "legal", "serve", "certs",
  "ai-security", "cyberwar", "modern", "llm-warfare", "platforms",
]);

function ensureDir(filepath) {
  mkdirSync(dirname(filepath), { recursive: true });
}

function makeMetaDescription(intro, fallback) {
  if (!intro) return fallback || "";
  const text = String(intro).replace(/\s+/g, " ").trim();
  if (text.length <= 200) return text;
  const truncated = text.slice(0, 200);
  const lastPeriod = truncated.lastIndexOf(". ");
  return lastPeriod > 100 ? truncated.slice(0, lastPeriod + 1) : truncated + "…";
}

function makeBreadcrumb(slug) {
  return [
    { label: "AtomEons", href: "/" },
    { label: "Learn", href: "/learn" },
    { label: "Cyber", href: "/learn/cyber" },
    { label: slug.replace(/-/g, " ") },
  ];
}

function makeTsx(page, slug) {
  const desc = makeMetaDescription(page.intro, page.subtitle);
  const breadcrumb = makeBreadcrumb(slug);

  return `import type { Metadata } from "next";
import { GenericPage } from "@/app/_components/generic-page/GenericPage";
import type { ContentPage } from "@/app/_components/generic-page/types";

// Auto-materialized from wf_dd652968-46e (cyber-deep-dives-50 workflow).
// Generated by .scripts/materialize-cyber-deep-dives.mjs.

const PAGE_DATA: ContentPage = ${JSON.stringify(page, null, 2)};

const BREADCRUMB = ${JSON.stringify(breadcrumb, null, 2)};

export const metadata: Metadata = {
  title: ${JSON.stringify(`${page.title} · /learn/cyber/${slug} · AtomEons`)},
  description: ${JSON.stringify(desc)},
  alternates: { canonical: ${JSON.stringify(`https://atomeons.com/learn/cyber/${slug}`)} },
  openGraph: {
    title: ${JSON.stringify(page.title)},
    description: ${JSON.stringify(page.subtitle || desc)},
    url: ${JSON.stringify(`https://atomeons.com/learn/cyber/${slug}`)},
    type: "article",
  },
  robots: { index: true, follow: true },
};

export default function Page() {
  const articleJsonLd = {
    "@context": "https://schema.org",
    "@type": "Article",
    "headline": ${JSON.stringify(page.title)},
    "description": ${JSON.stringify(page.subtitle || desc)},
    "author": { "@type": "Organization", "name": "AtomEons Systems Laboratory" },
    "publisher": { "@type": "Organization", "name": "AtomEons", "url": "https://atomeons.com" },
    "license": "https://creativecommons.org/licenses/by/4.0/",
    "isAccessibleForFree": true,
    "url": ${JSON.stringify(`https://atomeons.com/learn/cyber/${slug}`)},
  };
  return (
    <>
      <script type="application/ld+json" dangerouslySetInnerHTML={{ __html: JSON.stringify(articleJsonLd) }} />
      <GenericPage page={PAGE_DATA} breadcrumb={BREADCRUMB} />
    </>
  );
}
`;
}

let written = 0;
let skippedExisting = 0;
let skippedConflict = 0;
const writtenSlugs = [];

for (const page of pages) {
  const rawSlug = (page.slug || "").trim().toLowerCase().replace(/^\/+/, "").replace(/^learn\/cyber\//, "");
  if (!rawSlug) {
    console.log(`  SKIP — page has no slug: ${JSON.stringify(page).slice(0, 80)}`);
    continue;
  }

  if (EXISTING_PAGES.has(rawSlug)) {
    console.log(`  SKIP ${rawSlug} — would overwrite existing canonical cyber page`);
    skippedConflict++;
    continue;
  }

  const filePath = resolve(ROOT, rawSlug, "page.tsx");
  if (existsSync(filePath)) {
    console.log(`  SKIP ${rawSlug} — file exists`);
    skippedExisting++;
    continue;
  }

  ensureDir(filePath);
  writeFileSync(filePath, makeTsx(page, rawSlug), "utf8");
  console.log(`  ok    ${rawSlug}`);
  written++;
  writtenSlugs.push(rawSlug);
}

console.log("");
console.log(`✓ Wrote ${written} pages, skipped ${skippedExisting} existing files, ${skippedConflict} conflicts.`);
console.log(``);
console.log(`New routes:`);
for (const s of writtenSlugs) console.log(`  /learn/cyber/${s}`);
